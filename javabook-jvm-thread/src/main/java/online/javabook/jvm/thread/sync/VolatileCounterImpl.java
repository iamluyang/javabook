public final class VolatileCounterImpl implements ICounter {

	/**
	 * value
	 */
	private volatile long value = 0;

	@Override
	public long get() {
		return value;
	}

	@Override
	public long increment() {
		return value++;
	}

	@Override
	public long decrement() {
		return value--;
	}
}